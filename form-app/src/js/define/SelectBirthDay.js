export const SelectYear = [];
export const SelectMonth = [];
export const SelectDay = [];

// 年：1930年代から2021年代までを設定
for (let i = 1930; i <= 2021; i++) {
  SelectYear.push(i);
}
// 月
for (let i = 1; i <= 12; i++) {
  SelectMonth.push(i);
}
// 日
for (let i = 1; i <= 31; i++) {
  SelectDay.push(i);
}